"""Subprocess manager for autoexperiment.py."""
from __future__ import annotations

import json
import os
import signal
import subprocess
import sys
import time
from pathlib import Path
from typing import Any


class ExperimentRunner:
    """Launches and monitors autoexperiment.py as a subprocess."""

    def __init__(self, project_root: Path, scripts_dir: Path,
                 experiments_dir: Path):
        self.project_root = project_root
        self.scripts_dir = scripts_dir
        self.experiments_dir = experiments_dir
        self._process: subprocess.Popen | None = None
        self._pid_file = experiments_dir / "experiment.pid"

    @property
    def is_running(self) -> bool:
        if self._process is not None:
            return self._process.poll() is None
        # Check for orphaned process
        if self._pid_file.exists():
            try:
                pid = int(self._pid_file.read_text().strip())
                os.kill(pid, 0)  # Check if alive
                return True
            except (ValueError, ProcessLookupError, PermissionError):
                self._pid_file.unlink(missing_ok=True)
        return False

    def start(self, pcb_file: str, rounds: int, workers: int = 0,
              plateau: int = 3, seed: int | None = None,
              param_ranges: dict | None = None,
              score_weights: dict | None = None,
              extra_config: dict | None = None) -> int:
        """Start an experiment subprocess. Returns PID."""
        if self.is_running:
            raise RuntimeError("Experiment already running")

        # Remove stale stop file
        stop_file = self.experiments_dir / "stop.now"
        stop_file.unlink(missing_ok=True)

        # Build command
        autoexp = self.scripts_dir / "autoexperiment.py"
        pcb_path = self.project_root / pcb_file

        cmd = [
            sys.executable, str(autoexp),
            str(pcb_path),
            "--rounds", str(rounds),
            "--workers", str(workers),
            "--plateau", str(plateau),
            "--status-file", str(self.experiments_dir / "run_status.json"),
            "--log", str(self.experiments_dir / "experiments.jsonl"),
        ]
        if seed is not None:
            cmd += ["--seed", str(seed)]

        # Write program.md with param_ranges and score_weights
        program_path = self.scripts_dir / "program.md"
        program_data = {}
        if param_ranges:
            program_data["param_ranges"] = param_ranges
        if score_weights:
            program_data["score_weights"] = score_weights
        if extra_config:
            program_data.update(extra_config)

        if program_data:
            with open(program_path, "w") as f:
                f.write("# Experiment Program (auto-generated by GUI)\n\n")
                f.write("```json\n")
                f.write(json.dumps(program_data, indent=2))
                f.write("\n```\n")

        # Launch — use start_new_session so we can kill the entire process
        # group (autoexperiment + all its FreeRouting/worker children)
        self._process = subprocess.Popen(
            cmd,
            cwd=str(self.project_root),
            stdout=subprocess.PIPE,
            stderr=subprocess.STDOUT,
            text=True,
            start_new_session=True,
        )

        # Save PID
        self._pid_file.write_text(str(self._process.pid))
        return self._process.pid

    def stop(self) -> None:
        """Request graceful stop via signal file."""
        stop_file = self.experiments_dir / "stop.now"
        stop_file.touch()

    def kill(self) -> None:
        """Force kill the subprocess and its entire process group."""
        pid = None
        if self._process and self._process.poll() is None:
            pid = self._process.pid
        elif self._pid_file.exists():
            try:
                pid = int(self._pid_file.read_text().strip())
                os.kill(pid, 0)  # verify alive
            except (ValueError, ProcessLookupError, PermissionError):
                pid = None

        if pid is not None:
            try:
                pgid = os.getpgid(pid)
                os.killpg(pgid, signal.SIGTERM)
            except OSError:
                pass
            # Wait briefly, then SIGKILL if still alive
            deadline = time.monotonic() + 5
            while time.monotonic() < deadline:
                try:
                    os.kill(pid, 0)
                    time.sleep(0.2)
                except ProcessLookupError:
                    break
            else:
                try:
                    pgid = os.getpgid(pid)
                    os.killpg(pgid, signal.SIGKILL)
                except OSError:
                    pass

        if self._process:
            try:
                self._process.wait(timeout=2)
            except subprocess.TimeoutExpired:
                self._process.kill()
        self._pid_file.unlink(missing_ok=True)

    def read_status(self) -> dict:
        """Read current run_status.json."""
        status_path = self.experiments_dir / "run_status.json"
        if not status_path.exists():
            return {
                "phase": "idle",
                "round": 0,
                "total_rounds": 0,
                "progress_percent": 0,
                "workers": {"total": 0, "in_flight": 0, "idle": 0},
                "kept_count": 0,
                "best_score": 0,
                "latest_score": None,
                "elapsed_s": 0,
                "eta_s": 0,
                "maybe_stuck": False,
            }
        try:
            with open(status_path) as f:
                return json.load(f)
        except (json.JSONDecodeError, OSError):
            return {"phase": "error", "error": "Failed to read status file"}

    def read_latest_rounds(self, since_round: int = 0) -> list[dict]:
        """Read new rounds from experiments.jsonl since a given round number."""
        jsonl_path = self.experiments_dir / "experiments.jsonl"
        if not jsonl_path.exists():
            return []
        rounds = []
        try:
            with open(jsonl_path) as f:
                for line in f:
                    line = line.strip()
                    if not line:
                        continue
                    try:
                        rec = json.loads(line)
                        if rec.get("round_num", 0) > since_round:
                            rounds.append(rec)
                    except json.JSONDecodeError:
                        continue
        except OSError:
            pass
        return rounds

